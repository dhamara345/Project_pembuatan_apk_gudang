    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Lokasi");
        model.addColumn("Nama Rak");
        model.addColumn("Kapasitas");

    try {
        // Ambil kata kunci dari textfield cari
        String cari = txtcari.getText();
        
        // Query SQL dengan klausa LIKE untuk mencari berdasarkan Nama Rak atau ID
        String sql = "SELECT * FROM location_rack WHERE id_lokasi LIKE '%" + cari + "%' "
                   + "OR nama_rack LIKE '%" + cari + "%'";
        
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1),
                res.getString(2),
                res.getString(3)
            });
        }
        tbl_rak.setModel(model);
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Pencarian Gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

    private void tbl_rakMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_rakMouseClicked
        // TODO add your handling code here:
        int baris = tbl_rak.getSelectedRow();
        txtid_lokasi.setText(tbl_rak.getValueAt(baris, 0).toString());
    
        // Set item Combo Box berdasarkan data tabel
        String rak_terpilih = tbl_rak.getValueAt(baris, 1).toString();
        cmb_rak.setSelectedItem(rak_terpilih);
    
        txtkapasitas.setText(tbl_rak.getValueAt(baris, 2).toString());
        txtid_lokasi.setEditable(false); // Kunci ID Lokasi
    }//GEN-LAST:event_tbl_rakMouseClicked

