    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Lokasi");
    model.addColumn("Nama Rak");
    model.addColumn("Kapasitas");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery("SELECT * FROM location_rack");
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3) 
            });
        }
        tbl_rak.setModel(model); // Ganti tbl_rak sesuai nama variabel tabelmu
    } catch (Exception e) {
        System.out.println("Gagal load data: " + e.getMessage());
    }
    }
