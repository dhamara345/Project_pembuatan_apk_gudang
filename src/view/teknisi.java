    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("NIK Pegawai");
    model.addColumn("Nama Pegawai");
    model.addColumn("Jabatan");
    model.addColumn("Departemen");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery("SELECT * FROM Teknisi");
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4)
            });
        }
        tbl_teknisi.setModel(model); 
    } catch (Exception e) {
        System.out.println("Gagal load data: " + e.getMessage());
    }
    }
