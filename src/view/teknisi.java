    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
         try {
        String sql = "UPDATE Teknisi SET nama_pegawai=?, jabatan=?, departemen=? WHERE nik_pegawai=?";
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
        
        pst.setString(1, txtnm_pegawai.getText());
        pst.setString(2, cmb_jabatan.getSelectedItem().toString());
        pst.setString(3, txtdepartemen.getText());
        pst.setString(4, txtnik_pegawai.getText());
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Lokasi Berhasil Diupdate");
        datatable();
        reset_form();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_btn_editActionPerformed

