    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
         int confirm = JOptionPane.showConfirmDialog(null, "Hapus data Teknisi ini?", "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        try {
            // 2. Query DELETE untuk tabel location_rack
            String sql = "DELETE FROM Teknisi WHERE nik_pegawai = '" + txtnik_pegawai.getText() + "'";
            
            java.sql.Connection conn = new koneksi.koneksi().connect();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            
            // 3. Notifikasi sukses
            JOptionPane.showMessageDialog(null, "Data Teknisi Berhasil Dihapus");
            
            // 4. Refresh Tabel dan Bersihkan Form
            datatable();   
            reset_form();  
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Hapus Teknisi: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("NIK Pegawai");
        model.addColumn("Nama Pegawai");
        model.addColumn("Jabatan");
        model.addColumn("Departemen");

    try {
        // Ambil kata kunci dari textfield cari
        String cari = txtcari.getText();
        
        // Query SQL dengan klausa LIKE untuk mencari berdasarkan Nama Rak atau ID
        String sql = "SELECT * FROM Teknisi WHERE nik_pegawai LIKE '%" + cari + "%' "
                   + "OR nama_rack LIKE '%" + cari + "%'";
        
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1),
                res.getString(2),
                res.getString(3),
                res.getString(4)
            });
        }
        tbl_teknisi.setModel(model);
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Pencarian Gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

    private void tbl_teknisiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_teknisiMouseClicked
        // TODO add your handling code here:
        int baris = tbl_teknisi.getSelectedRow();
        txtnik_pegawai.setText(tbl_teknisi.getValueAt(baris, 0).toString());
        txtnm_pegawai.setText(tbl_teknisi.getValueAt(baris, 1).toString());
    
        // Set item Combo Box berdasarkan data tabel
        String jabatan_terpilih = tbl_teknisi.getValueAt(baris, 2).toString();
        cmb_jabatan.setSelectedItem(jabatan_terpilih);
    
        txtdepartemen.setText(tbl_teknisi.getValueAt(baris, 3).toString());
        txtnik_pegawai.setEditable(false); // Kunci ID Lokasi
    }//GEN-LAST:event_tbl_teknisiMouseClicked

