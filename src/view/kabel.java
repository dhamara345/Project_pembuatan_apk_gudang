    private void tbl_kabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_kabelMouseClicked
    // TODO add your handling code here:                                      
    int baris = tbl_kabel.getSelectedRow();
    
    // Mengambil data dari tabel ke TextField
    txt_id.setText(tbl_kabel.getValueAt(baris, 0).toString());
    txt_pjgkabel.setText(tbl_kabel.getValueAt(baris, 1).toString());
    
    // Mengambil data untuk Radio Button (Jenis Perangkat)
    String jenis = tbl_kabel.getValueAt(baris, 2).toString();
    if(jenis.equals("LAN")){
    rb_lan.setSelected(true);
    } else if(jenis.equals("FO")){
        rb_fo.setSelected(true);
    } else if(jenis.equals("Console")){
        rb_console.setSelected(true);
    }
    
    txt_tpkabel.setText(tbl_kabel.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_tbl_kabelMouseClicked

    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
            String sql = "DELETE FROM Kabel WHERE id_kabel = '" + txt_id.getText() + "'";
            java.sql.Connection conn = new koneksi.koneksi().connect();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
        
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            datatable();   // Refresh tabel
            reset_form();  // Kosongkan form
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Hapus: " + e.getMessage());
            }
         }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
    // TODO add your handling code here:
        try {
        String jenis = "";
        if (rb_lan.isSelected()) { jenis = "LAN"; }
        else if (rb_fo.isSelected()) { jenis = "FO"; }
        else if (rb_console.isSelected()) { jenis = "Console"; }

        String sql = "UPDATE Kabel SET panjang_kabel=?, jenis_kabel=?, tipe_kabel=? WHERE id_kabel=?";
    
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
        pst.setString(1, txt_pjgkabel.getText());
        pst.setString(2, jenis);
        pst.setString(3, txt_tpkabel.getText());
        pst.setString(4, txt_id.getText());
    
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
        datatable();
        reset_form();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Edit: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:                                      
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Kabel");
    model.addColumn("Nama Kabel");
    model.addColumn("Jenis");
    model.addColumn("Dimensi");

    try {
        // Query menggunakan LIKE untuk mencari kata yang mengandung input dari txt_cari
        String sql = "SELECT * FROM Kabel WHERE panjang_kabel LIKE '%" + txt_cari.getText() + "%' "
                   + "OR id_kabel LIKE '%" + txt_cari.getText() + "%'";
        
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4)
            });
        }
        tbl_kabel.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Pencarian Gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

