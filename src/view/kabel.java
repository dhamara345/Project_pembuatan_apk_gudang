    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Kabel");
    model.addColumn("Panjang Kabel");
    model.addColumn("Jenis");
    model.addColumn("Tipe Kabel");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery("SELECT * FROM Kabel");
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4)
            });
        }
        tbl_kabel.setModel(model); // Ganti tbl_barang sesuai nama variabel tabelmu
    } catch (Exception e) {
        System.out.println("Gagal load data: " + e.getMessage());
    }
}

