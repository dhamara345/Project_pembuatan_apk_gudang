    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Client");
    model.addColumn("Nama Perusahan");

    try {
        String cari = txt_cari_popup.getText();
        koneksi koneksi = new koneksi(); 
        java.sql.Connection conn = koneksi.connect();
        String sql = "SELECT id_client, nama_perusahaan FROM client " +
                     "WHERE id_client LIKE '%"+cari+"%' OR nama_perusahaan LIKE '%"+cari+"%'";
        
      java.sql.ResultSet res = conn.createStatement().executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{
                    res.getString(1), // id_supplier
                    res.getString(2)  // nama_perusahaan
                });
            }
            tbl_client_popup.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Gagal load client: " + e.getMessage());
        }
    }

