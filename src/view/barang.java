    private void tbl_supplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_supplierMouseClicked
    // TODO add your handling code here:                                      
    int baris = tbl_supplier.getSelectedRow();
    
    // Mengambil data dari tabel ke TextField
    txt_sn.setText(tbl_supplier.getValueAt(baris, 0).toString());
    txt_nama.setText(tbl_supplier.getValueAt(baris, 1).toString());
    
    // Mengambil data untuk Radio Button (Jenis Perangkat)
    String jenis = tbl_supplier.getValueAt(baris, 2).toString();
    if(jenis.equals("Router")){
    rb_router.setSelected(true);
    } else if(jenis.equals("Switch")){
        rb_switch.setSelected(true);
    } else if(jenis.equals("Server")){
        rb_server.setSelected(true);
    }
    
    txt_dimensi.setText(tbl_supplier.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_tbl_supplierMouseClicked

    private void btn_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_removeActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus data ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
            String sql = "DELETE FROM Perangkat WHERE sn_perangkat = '" + txt_sn.getText() + "'";
            java.sql.Connection conn = new koneksi.koneksi().connect();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
        
            JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
            datatable();   // Refresh tabel
            reset_form();  // Kosongkan form
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal Hapus: " + e.getMessage());
            }
         }
    }//GEN-LAST:event_btn_removeActionPerformed

    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
    // TODO add your handling code here:
        try {
        String jenis = "";
        if (rb_router.isSelected()) { jenis = "Router"; }
        else if (rb_switch.isSelected()) { jenis = "Switch"; }
        else if (rb_server.isSelected()) { jenis = "Server"; }

        String sql = "UPDATE Perangkat SET nama_perangkat=?, jenis_perangkat=?, Dimensi=? WHERE sn_perangkat=?";
    
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
        pst.setString(1, txt_nama.getText());
        pst.setString(2, jenis);
        pst.setString(3, txt_dimensi.getText());
        pst.setString(4, txt_sn.getText());
    
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
        datatable();
        reset_form();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Edit: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cariActionPerformed
        // TODO add your handling code here:                                      
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("SN Perangkat");
    model.addColumn("Nama Perangkat");
    model.addColumn("Jenis");
    model.addColumn("Dimensi");

    try {
        // Query menggunakan LIKE untuk mencari kata yang mengandung input dari txt_cari
        String sql = "SELECT * FROM Perangkat WHERE nama_perangkat LIKE '%" + txt_cari.getText() + "%' "
                   + "OR sn_perangkat LIKE '%" + txt_cari.getText() + "%'";
        
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4)
            });
        }
        tbl_supplier.setModel(model);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Pencarian Gagal: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cariActionPerformed

