    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("SN Perangkat");
    model.addColumn("Nama Perangkat");
    model.addColumn("Jenis");
    model.addColumn("Dimensi");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery("SELECT * FROM Perangkat");
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4)
            });
        }
        tbl_supplier.setModel(model); // Ganti tbl_barang sesuai nama variabel tabelmu
    } catch (Exception e) {
        System.out.println("Gagal load data: " + e.getMessage());
    }
}

