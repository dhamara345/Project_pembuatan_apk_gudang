    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Perangkat");
    model.addColumn("Nama Supplier");
    model.addColumn("Alamat Supplier");
    model.addColumn("NO Telepon");
    model.addColumn("Nama PIC");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        java.sql.ResultSet res = stm.executeQuery("SELECT * FROM Supplier");
        while (res.next()) {
            model.addRow(new Object[]{
                res.getString(1), 
                res.getString(2), 
                res.getString(3), 
                res.getString(4),
                res.getString(5)
            });
        }
        tbl_supplier.setModel(model); 
    } catch (Exception e) {
        System.out.println("Gagal load data: " + e.getMessage());
    }
    }
