    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        // TODO add your handling code here:
     try {
        String sql = "UPDATE Supplier SET nama_supplier=?, alamat_supplier=?, no_telepon=?, nama_PIC=? WHERE id_supplier=?";
    
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.PreparedStatement pst = conn.prepareStatement(sql);
    
        // Mengisi parameter sesuai urutan tanda tanya (?) di atas
        pst.setString(1, txtnm_supplier.getText());
        pst.setString(2, txtalamat_supplier.getText());
        pst.setString(3, txtno_telepon.getText());
        pst.setString(4, txtnm_pic.getText());
        pst.setString(5, txtid_supplier.getText()); 
    
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data Supplier Berhasil Diubah");
        
        datatable();    // Memanggil fungsi tampil data agar tabel refresh
        reset_form();   // Memanggil fungsi kosongkan form agar inputan bersih
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Edit Data Supplier: " + e.getMessage());
    }

    }//GEN-LAST:event_btn_editActionPerformed

