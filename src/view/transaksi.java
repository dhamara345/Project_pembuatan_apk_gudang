    private void datatable() {
    DefaultTableModel model = new DefaultTableModel();
    // 1. Tambah kolom baru di sini biar tabelnya makin lebar dan detail
    model.addColumn("ID Transaksi");
    model.addColumn("Tanggal");
    model.addColumn("ID Barang"); 
    model.addColumn("Jenis");
    model.addColumn("Jumlah");
    model.addColumn("Supplier"); // Kolom Baru
    model.addColumn("Client");   // Kolom Baru
    model.addColumn("Teknisi");  // Kolom Baru
    model.addColumn("Admin");
    
    try {
        java.sql.Connection conn = new koneksi.koneksi().connect();
        java.sql.Statement stm = conn.createStatement();
        
        // Ambil semua data dari tabel Transaksi, urutkan dari yang paling baru
        String sql = "SELECT * FROM Transaksi ORDER BY id_transaksi DESC";
        java.sql.ResultSet res = stm.executeQuery(sql);
        
        while (res.next()) {
            // Logika gabungan: Kalau sn_perangkat kosong, ambil id_kabel
            String idBarang = res.getString("sn_perangkat");
            if (idBarang == null || idBarang.isEmpty() || idBarang.equals("")) {
                idBarang = res.getString("id_kabel");
            }

            // Memastikan data string kosong/null diubah jadi "-" biar rapi di tabel
            String supplier = res.getString("id_supplier");
            if (supplier == null || supplier.isEmpty()) supplier = "-";
            
            String client = res.getString("id_client");
            if (client == null || client.isEmpty()) client = "-";
            
            String teknisi = res.getString("nik_pegawai");
            if (teknisi == null || teknisi.isEmpty()) teknisi = "-";

            // 2. Masukkan semua data ke dalam baris tabel (URUTAN HARUS SAMA DENGAN ADDCOLUMN DI ATAS!)
            model.addRow(new Object[]{
                res.getString("id_transaksi"),
                res.getString("tgl_transaksi"), 
                idBarang, 
                res.getString("jenis_transaksi"),
                res.getString("jumlah"),
                supplier, // Data Supplier masuk
                client, // Data Client masuk
                teknisi, // Data NIK Teknisi masuk
                res.getString("username") // Ini kolom admin di DB lu
            });
        }
        tbl_transaksi.setModel(model); 
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal load data tabel: " + e.getMessage());
    }
}

