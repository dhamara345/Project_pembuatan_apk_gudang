    private void btn_cetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_cetakActionPerformed
        // TODO add your handling code here:
        try {
        // 1. Ambil ID dari baris tabel yang sedang diklik secara langsung biar akurat!
        int baris = tbl_transaksi.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(null, "Silakan klik/pilih salah satu baris data di tabel bawah dulu, bro!");
            return;
        }
        
        // Mengambil nilai ID Transaksi langsung dari kolom ke-0 tabel yang lu klik
        String idTarget = tbl_transaksi.getValueAt(baris, 0).toString();
        
        // 2. Jalur file report lu
        String jrxmlPath = "src/report/report_transaksi.jrxml";
        String jasperPath = "src/report/report_transaksi.jasper";
        
        // Compile otomatis
        net.sf.jasperreports.engine.JasperCompileManager.compileReportToFile(jrxmlPath, jasperPath);
        
        // 3. Masukkan ID Target tadi ke dalam parameter database
        java.util.HashMap<String, Object> parameters = new java.util.HashMap<>();
        parameters.put("id_transaksi", idTarget); // Menembak ID yang lu sorot biru (Misal: TRX0001)
        
        // 4. Hubungkan database dan cetak
        java.sql.Connection conn = new koneksi.koneksi().connect();
        net.sf.jasperreports.engine.JasperPrint print = net.sf.jasperreports.engine.JasperFillManager.fillReport(jasperPath, parameters, conn);
        net.sf.jasperreports.view.JasperViewer.viewReport(print, false); 
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal Mencetak Laporan: " + e.getMessage());
    }
    }//GEN-LAST:event_btn_cetakActionPerformed

